# ╔═╡ d56d6b34-2a44-401b-81c7-636e438fe556
function to_precise_hours(nanoseconds)
	minutes = Dates.Minute(nanoseconds).value
	hours = minutes/60
end

# ╔═╡ 98365a66-f632-4981-bd98-0fa7f01c4c04
# https://stackoverflow.com/a/69782285/10595676
round_step(x, step) = round(x / step) * step

# ╔═╡ 14ba9890-fdfb-439d-abb6-00148bfd7d13
function to_halfhours(nanoseconds)
	hours = to_precise_hours(nanoseconds)
	halfhours = round_step(hours, 0.5)
end

# ╔═╡ b5b3b297-3466-4f7f-8e0c-d83493d213e4
# The new system will let you add "X" hours to multiple days at once.
# It's a weird UI, but this should make it quick. 
# It took me way too long to do by hand today.
function get_times_to_write_in(entries)
	precise_time = to_precise_hours(sum([time for (date, time) in entries]))
	@info "" precise_time
	entries = [(date, to_halfhours(worktime)) for (date, worktime) in entries]
	time_when_rounded = sum(halfhours for (_, halfhours) in entries)
	@info "" time_when_rounded
	
	result = Dict(halfhours => Date[] for (_, halfhours) in entries)
	for (date, halfhours) in entries
		push!(result[halfhours], date)
	end

	for (halfhours, dates) in result
		sort!(dates)
	end

	for halfhours in sort(collect(keys(result)), rev=true)
		@info "hours: $halfhours", result[halfhours]
	end
end

