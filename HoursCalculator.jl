function get_date(match)
	y, m, d = match[:year], match[:month], match[:day]
	y, m, d = (parse(Int, x) for x in (y, m, d))
	Date(y + 2000, m, d)
end

# ╔═╡ 51b1bb44-e8ec-495d-b586-500c5bd86f88
function get_start_time(match)
	HH, MM = match[:start_hour], match[:start_minute]
	HH, MM = (parse(Int, x) for x in (HH, MM))
	Time(HH, MM)
end

# ╔═╡ a6fbfdb5-ab55-4779-8c51-6d85acfc2373
function get_end_time(match)
	HH, MM = match[:end_hour], match[:end_minute]
	HH, MM = (parse(Int, x) for x in (HH, MM))
	Time(HH, MM)
end

# ╔═╡ 0e9c5c7c-fb43-4f0e-9551-1723a1f8ef94
function get_time(match)
	get_end_time(match) - get_start_time(match)
end

